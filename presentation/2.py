a = int(input("Введите число первой стороны треугольника: "))
b = int(input("Введите число второй стороны треугольника: "))
c = int(input("Введите число третьей стороны треугольника: "))

if a == b == c:
    print("Равносторонний")
elif a != b != c:
    print("Разносторонний")
else:
    print("Равнобедренный")
